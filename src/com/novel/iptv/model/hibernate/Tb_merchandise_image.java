public class Tb_merchandise_image {
	private String id;
	private String merchandise_code;
	private String image_path;
	private int type;					
	private int idx;
	private int  image_size_byte;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMerchandise_code() {
		return merchandise_code;
	}
	public void setMerchandise_code(String merchandiseCode) {
		merchandise_code = merchandiseCode;
	}
	public String getImage_path() {
		return image_path;
	}
	public void setImage_path(String imagePath) {
		image_path = imagePath;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getImage_size_byte() {
		return image_size_byte;
	}
	public void setImage_size_byte(int imageSizeByte) {
		image_size_byte = imageSizeByte;
	}
}
